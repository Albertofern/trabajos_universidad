				Ejecutivo de coalición.<br/><br/>
				Durante su turno de réplica en la sesión de investidura en el Senado, Letta, cuyo Gobierno recibió ya 
				ayer la aprobación de la Cámara de los Diputados, dijo también que se han generado <cite>"excesivas"</cite> expectativas 
				sobre la formación de su Ejecutivo, pues con ella no se resuelve de un plumazo la <cite>"frágil"</cite> situación de 
				<cite>"emergencia"</cite> que vive Italia.<br/><br/>
				En la Unión Europea <cite>"tenemos que avanzar juntos. Se ha dejado que fuera solo la moneda lo que nos unía, 
				pero un continente como el nuestro no puede estar solo unido por la moneda"</cite>, dijo el nuevo primer ministro, 
				del Partido Demócrata (PD) de centroizquierda.<br/><br/>
				<cite>"El asunto de la crisis europea <strong>-agregó-</strong> es un asunto, sobre todo, de democracia, de soberanía, de 
				eficacia de respuestas. Estoy convencido de que no es una cuestión técnica, es una cuestión de hacer 
				entender a la burocracia de Bruselas, a los líderes políticos europeos y a los ciudadanos, que nuestro 
				destino o es común o estará hecho de países que caminarán cada uno por su lado"</cite>.<br/><br/>
				Letta, quien ha dedicado gran parte de su programa de Gobierno a los asuntos europeos, afirmó que el 
				problema es que, justo en el <cite>"peor momento de la crisis"</cite>, se ha visto que del <cite>"diseño originario europeo"</cite> 
				solo se ha confirmado de pleno la moneda única, el euro, y el resto o ha quedado <cite>"estancado o no ha avanzado 
				lo suficiente"</cite>.<br/><br/>
				Tras tener las primeras reacciones al discurso de investidura que pronunció ayer en la Cámara de los 
				Diputados, donde su Gobierno fue investido con una amplia mayoría, el nuevo primer ministro quiso alertar 
				a las fuerzas parlamentarias para que no cayeran en las complacencias tras haber puesto fin al bloqueo 
				político.<br/><br/>
				<cite>"Me he dado cuenta de que hay un gran problema: hay una carga de expectativas excesiva sobre este 
				Gobierno. Si no hay consciencia de la objetiva fragilidad de lo que se ha hecho y de lo que estamos 
				haciendo y se piensa que todos los problemas ya están resueltos formando un Gobierno, creo que nos 
				equivocamos"</cite>, aseveró Letta.<br/><br/>
				<cite>"La situación que tenemos ante nosotros sigue siendo de grandísima dificultad y de grandísima 
				emergencia. La situación es fruto de esta situación de gran emergencia"</cite>, incidió el jefe del Gobierno.<br/><br/>
				El político demócrata, quien se ha dado 18 meses de plazo para empezar a ver resultados de las 
				reformas de su Gobierno, explicó que en las visitas que realizará desde hoy y hasta el jueves a Berlín, 
				París y Bruselas tratará de explicar lo que ha ocurrido en Italia en los últimos cinco días, hasta la 
				formación del Ejecutivo, <cite>"un ejercicio para nada sencillo"</cite>.<br/><br/>
				Letta aseguró además que gran parte del problema económico de Italia, que cerró 2012 con un retroceso 
				del 2,4 % del PIB, se basa en la falta de confianza de los ciudadanos y las empresas que no se han visto 
				tan afectadas por la crisis a mantener su nivel de consumo e inversión por algunas decisiones políticas.<br/><br/>
				Por ello que su Gobierno se plantea, como uno de los principales puntos a abordar, la recuperación de 
				la credibilidad de la clase política.<br/><br/>
