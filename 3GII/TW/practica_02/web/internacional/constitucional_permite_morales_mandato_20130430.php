				el Parlamento, a instancias del partido de Morales.<br/><br/>
				La decisión del tribunal ha sido criticada de inmediato por la oposición que rechazó un fallo que 
				considera <cite>"inaceptable"</cite>.<br/><br/>
				La sentencia respalda la postulación de Morales y del vicepresidente, Álvaro García Linera, por 
				considerar que el actual mandato que comenzaron el 22 de enero de 2010 cuenta como primero del Estado 
				plurinacional, tras la refundación del país en 2009 con la promulgación de una nueva Carta Magna.
				<cite>"Se ha realizado la refundación del Estado como un Estado Plurinacional y esa refundación emerge de 
				un poder constituyente que ha generado una nueva Constitución Política del Estado que contempla un nuevo 
				orden que contiene la aplicación de la Constitución"</cite>, dijo Flores a los periodistas.<br/><br/>
				La Constitución limita a dos el número de mandatos consecutivos que puede ejercer un presidente en 
				Bolivia, pero Morales siempre ha defendido que el primero de sus gobiernos (2006-2010) no es computable 
				debido a que tuvo lugar antes de la refundación de su país y a que no completó el período legal de cinco 
				años.<br/><br/>
				Con este argumento, y a pesar de que nunca ha confirmado que será candidato, el presidente Morales 
				ha llegado a asegurar que la consulta al Constitucional era innecesaria y ha aceptado en numerosas 
				ocasiones ser proclamado por sus seguidores.<br/><br/>
				De ganar en los comicios de diciembre del 2014, Morales gobernará hasta el 2020 y se convertiría así 
				en el presidente que más años ha permanecido en el poder en Bolivia.<br/><br/>
