            <p class="cont_noticia"> ERC, Amaiur y BNG han presentado hoy en el Congreso de los Diputados una proposición no de ley para 
				permitir el reconocimiento del derecho de autodeterminación de Cataluña, País Vasco y Galicia, y han acusado 
				al Gobierno de convertir la Constitución en la inquisición y el TC en un tribunal inquisitorial. <br/><br/>
				Los tres partidos han registrado esta mañana en el Congreso de los Diputados esta proposición no de ley, 
				meses después de que les fuese rechazada por la Mesa la posibilidad de tramitar una iniciativa parecida en 
				forma de proposición de ley. <br/><br/>
				El argumento principal que utilizan los tres partidos es que el Estado ha suscrito varios tratados 
				internacionales en los que se reconoce el derecho de autodeterminación de los pueblos y que, sin embargo, no 
				lo ha traspuesto en la legislación española, por lo se trata de llenar un <cite>"vacío legal"</cite>. <br/><br/>
				La proposición no de ley ha sido presentada este mediodía en una rueda de prensa conjunta realizada en 
				Barcelona por los diputados Alfred Bosch, de ERC; Jon Iñarritu, de Amaiur, y Rosana Pérez, del BNG, en la 
				que han reclamado su derecho como parlamentarios a presentar iniciativas sin que el PP utilice su mayoría 
				absoluta para bloquear su tramitación. <br/><br/>
				<cite>"Poner trabas"</cite> al reconocimiento del derecho de 
				autodeterminación y <cite>"recurrir cada paso que se hace para avanzar hacia el ejercicio de este derecho, en 
				lugar de dialogar por ejemplo con el Gobierno catalán, provoca que los procesos soberanistas se 
				acentúen"</cite>, declaraba el portavoz de ERC, Alfred Bosch.
				<cite>"El problema <strong>-ha afirmado-</strong> es que el Gobierno del PP convierte la Constitución en la inquisición y el 
				Tribunal Constitucional (TC) en un tribunal inquisitorial, y usa ambos instrumentos para decir que <strong>'no'</strong> y 
				cerrar las puertas, aunque con esta actitud imposibilitan cualquier debate y acuerdo".<br/><br/>
				<cite>"Si este es el camino <strong>-ha añadido Bosch-</strong>, aquellos que esperaban que la Constitución española fuese 
				utilizada como un marco y una casa de las libertades se encontrarán con que pueda acabar siendo una jaula 
				de pueblos"</cite>. <br/><br/>
				Por parte de Amaiur, el diputado Jon Iñarritu ha afirmado que el PP hace <cite>"un ejercicio de intolerancia"</cite> 
				al negar el debate en el Congreso de propuestas de reconocimiento del derecho de autodeterminación, lo que 
				no evita que <cite>"cada vez sean más las naciones que quieren decidir su futuro"</cite>. <br/><br/>
				Después de considerar que Cataluña va ahora mismo por delante en el proceso soberanista, y que desde 
				Euskadi se contemplan los pasos que está dando <cite>"con sana envidia"</cite>, Iñarritu ha lamentado que el PNV <cite>"mire 
				más hacia el PSE y el PP que no hacia la izquierda abertzale"</cite>.<br/><br/>
				Ha admitido, no obstante, que <cite>"hay distintas velocidades"</cite> y que en Euskadi se otorga prioridad a 
				conseguir que la pacificación de la sociedad vasca culmine con éxito.<br/><br/>
