				impulsa el Ejecutivo regional. La pregunta que se planteará a la sociedad es: ¿Está usted a favor de una 
				sanidad de gestión pública, de calidad y universal, y en contra de su privatización y de las leyes que lo 
				permiten?<br/><br/>
				Además, los organizadores utilizarán el resultado de la consulta, si éste refleja oposición a la 
				externalización, para articular una iniciativa ciudadana que se remitirá a la Asamblea de Madrid, a las 
				Cortes Generales, y al Parlamento Europeo.<br/><br/>
				En este sentido, se han diseñado las hojas de recogida de firmas de conformidad con el reglamento del 
				Parlamento Europeo, que establece un plazo de 18 meses para la validez y utilización de las firmas. Además, 
				se podrá rellenar un formulario para que los participantes puedan detallar también su correo electrónico, 
				su Facebook o su cuenta en Twitter para elaborar una especie de <cite>"censo"</cite> y contactar mediante las redes 
				sociales con éstas personas para otras actuaciones similares.<br/><br/>
				Delegación del Gobierno ha recibido, de momento, la solicitud de hasta 2.900 altas para constitución 
				de mesas y un total de 1.900 voluntarios han confirmado que participarán en el desarrollo de la consulta. 
				El plazo para inscribirse como voluntario finaliza este martes, 30 de abril. <cite>"En especial, hace falta que 
				haya mesas en colegios, institutos, etc"</cite>, afirman los organizadores.<br/><br/>
				Aparte de las mesas, estos voluntarios recorrerán calles y plazas para lograr que la gente se pronuncie 
				mientras que en los grandes hospitales, centros de salud y lugares más representativos de los municipios 
				de la región se habilitarán las mesas, si se obtiene la autorización.<br/><br/>
				Por otro lado, los organizadores también quieren que la consulta sea una <cite>"fiesta de la democracia"</cite> y, 
				así, los jóvenes de menos de 18 años podrán entregar un escrito recogiendo una frase por la sanidad y los 
				niños también podrán entregar en las urnas dibujos.<br/><br/>
